describe("analyzeRatings", () => {
  test("splits frustrations (<=4) and successes (>=7); ratings 5-6 excluded from both", () => {
    const res = analyzeRatings(
      [
        rating({ rating: 4, sentiment_summary: "slow, minutes wasted" }),
        rating({ rating: 5, sentiment_summary: "quick and clean" }), // neither bucket
        rating({ rating: 6, sentiment_summary: "wrong approach" }), // neither bucket
        rating({ rating: 7, sentiment_summary: "quick efficient smooth" }),
      ],
      "Weekly",
    );
    expect(res.frustrations.map((f) => f.pattern)).toContain("Time/Performance Issues");
    expect(res.successes.map((s) => s.pattern)).toContain("Quick Resolution");
    // rating 6 "wrong approach" must NOT surface (only <=4 feed frustrations)
    expect(res.frustrations.map((f) => f.pattern)).not.toContain("Wrong Approach");
    expect(res.avgRating).toBeCloseTo((4 + 5 + 6 + 7) / 4, 10);
  });

  test("recommendation map fires per detected frustration label", () => {
    const res = analyzeRatings([rating({ rating: 2, sentiment_summary: "slow, took hours" })], "Weekly");
    expect(res.recommendations).toContain("Consider setting clearer time expectations and progress updates");
  });

  test("no frustrations → the fallback recommendation", () => {
    const res = analyzeRatings([rating({ rating: 9, sentiment_summary: "quick clean elegant" })], "Weekly");
    expect(res.recommendations).toEqual(["Continue current patterns - no major issues detected"]);
  });

  test("empty ratings → zeroed result", () => {
    const res = analyzeRatings([], "Weekly");
    expect(res).toEqual({
      period: "Weekly",
      totalRatings: 0,
      avgRating: 0,
      frustrations: [],
      successes: [],
      topIssues: [],
      recommendations: [],
    });
  });
});

