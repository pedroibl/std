// Byte-parity: scoreRules detection vs the original detectPatterns
// ============================================================================

describe("detectPatterns byte-parity (scoreRules vs original)", () => {
  test("frustration buckets: same keys, order, and members", () => {
    const got = detectPatterns(SUMMARIES, FRUSTRATION_RULES);
    const want = detectPatternsOriginal(SUMMARIES, FRUSTRATION_PATTERNS);
    expect([...got.keys()]).toEqual([...want.keys()]);
    for (const k of want.keys()) expect(got.get(k)).toEqual(want.get(k));
  });

  test("success buckets: same keys, order, and members", () => {
    const got = detectPatterns(SUMMARIES, SUCCESS_RULES);
    const want = detectPatternsOriginal(SUMMARIES, SUCCESS_PATTERNS);
    expect([...got.keys()]).toEqual([...want.keys()]);
    for (const k of want.keys()) expect(got.get(k)).toEqual(want.get(k));
  });

  test("top label + full bucket ranking match on a frustration-heavy corpus", () => {
    // Skew so one label dominates → assert the derived top-of-ranking is identical both ways.
    const corpus = [
      "slow slow took hours", // Time/Performance
      "delay, long wait", // Time/Performance
      "hang and wait", // Time/Performance
      "a crash bug", // Tool/System Failures
    ];
    const got = detectPatterns(corpus, FRUSTRATION_RULES);
    const want = detectPatternsOriginal(corpus, FRUSTRATION_PATTERNS);
    const rank = (m: Map<string, string[]>) =>
      [...m.entries()].sort((a, b) => b[1].length - a[1].length).map(([k, v]) => `${k}:${v.length}`);
    expect(rank(got)).toEqual(rank(want));
    expect(rank(got)[0]).toBe("Time/Performance Issues:3");
  });

  test("empty input → empty map", () => {
    expect([...detectPatterns([], FRUSTRATION_RULES).entries()]).toEqual([]);
  });
});

