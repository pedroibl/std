function seedRatings(root: string, rows: Rating[]): void {
  mkdirSync(root, { recursive: true });
  writeFileSync(join(root, "ratings.jsonl"), rows.map((r) => JSON.stringify(r)).join("\n") + "\n");
}

describe("main", () => {
  test("--help prints usage and returns 0 without reading ratings", () => {
    const root = fixtureRoot();
    const deps = depsFor(root);
    expect(main(["--help"], deps)).toBe(0);
    const logged = (deps as Deps & { logged: string[] }).logged;
    expect(logged.join("\n")).toContain("Aggregate ratings into actionable patterns");
  });

  test("missing ratings file → 0 and 'No ratings file found'", () => {
    const root = fixtureRoot();
    const deps = depsFor(root);
    expect(main(["--week"], deps)).toBe(0);
    const logged = (deps as Deps & { logged: string[] }).logged;
    expect(logged.some((l) => l.startsWith("No ratings file found at:"))).toBe(true);
  });

  test("--week default: filters last 7 days and writes a weekly report", () => {
    const root = fixtureRoot();
    seedRatings(root, [
      rating({ rating: 2, timestamp: "2026-07-11T00:00:00Z", sentiment_summary: "slow minutes" }), // in window
      rating({ rating: 3, timestamp: "2026-06-01T00:00:00Z", sentiment_summary: "wrong approach" }), // out (>7d)
    ]);
    const deps = depsFor(root);
    expect(main([], deps)).toBe(0); // no flag == week
    const out = join(root, "SYNTHESIS", "2026-07", "2026-07-12_weekly-patterns.md");
    expect(existsSync(out)).toBe(true);
    const body = readFileSync(out, "utf-8");
    expect(body).toContain("**Period:** Weekly");
    expect(body).toContain("Time/Performance Issues"); // from the in-window rating
    expect(body).not.toContain("Wrong Approach"); // filtered out by window
  });

  test("--month widens the window", () => {
    const root = fixtureRoot();
    seedRatings(root, [
      rating({ rating: 3, timestamp: "2026-06-20T00:00:00Z", sentiment_summary: "wrong approach mistake" }), // ~22d
    ]);
    const deps = depsFor(root);
    expect(main(["--month"], deps)).toBe(0);
    const out = join(root, "SYNTHESIS", "2026-07", "2026-07-12_monthly-patterns.md");
    expect(existsSync(out)).toBe(true);
    expect(readFileSync(out, "utf-8")).toContain("Wrong Approach");
  });

  test("--all includes everything valid; invalid timestamps dropped", () => {
    const root = fixtureRoot();
    seedRatings(root, [
      rating({ rating: 2, timestamp: "2020-01-01T00:00:00Z", sentiment_summary: "slow" }),
      rating({ rating: 2, timestamp: "not-a-date", sentiment_summary: "crash bug" }),
    ]);
    const deps = depsFor(root);
    expect(main(["--all"], deps)).toBe(0);
    const out = join(root, "SYNTHESIS", "2026-07", "2026-07-12_all-time-patterns.md");
    const body = readFileSync(out, "utf-8");
    expect(body).toContain("Time/Performance Issues"); // the valid old one
    expect(body).not.toContain("Tool/System Failures"); // invalid-timestamp rating dropped
  });

  test("--dry-run does not write a file but still returns 0", () => {
    const root = fixtureRoot();
    seedRatings(root, [rating({ rating: 2, timestamp: "2026-07-11T00:00:00Z", sentiment_summary: "slow minutes" })]);
    const deps = depsFor(root);
    expect(main(["--week", "--dry-run"], deps)).toBe(0);
    expect(existsSync(join(root, "SYNTHESIS"))).toBe(false);
    const logged = (deps as Deps & { logged: string[] }).logged;
    expect(logged.some((l) => l.includes("DRY RUN"))).toBe(true);
  });

  test("empty window → 0 and 'No ratings in this period', no file", () => {
    const root = fixtureRoot();
    seedRatings(root, [rating({ rating: 2, timestamp: "2020-01-01T00:00:00Z", sentiment_summary: "slow" })]);
    const deps = depsFor(root);
    expect(main(["--week"], deps)).toBe(0);
    expect(existsSync(join(root, "SYNTHESIS"))).toBe(false);
    const logged = (deps as Deps & { logged: string[] }).logged;
    expect(logged.some((l) => l.includes("No ratings in this period"))).toBe(true);
  });
});

